namespace IntralismScoreChecker
{
    /// <summary>
    ///     The enum that represents if a intralism map is broken, not broken or if it's unknown.
    /// </summary>
    public enum BrokenType
    {
        /// <summary>
        ///     Indicates that the Intralism map is broken.
        /// </summary>
        Broken,

        /// <summary>
        ///     Indicates that the Intralism map is not broken.
        /// </summary>
        Not_Broken,

        /// <summary>
        ///     Indicates that it's unknown if the Intralism map is broken.
        /// </summary>
        Idk,
    }
}